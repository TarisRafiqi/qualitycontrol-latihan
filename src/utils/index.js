// soal nomor 1 PENJUMLAHAN
export const penjumlahan = (variabelSatu, variabelDua) => {
    return variabelSatu + variabelDua;
};

// soal nomor 2 PENGURANGAN
export const pengurangan = (variabelSatu, variabelDua) => {
    return variabelSatu - variabelDua;
};

// soal nomor 3 PEMBAGIAN
export const pembagian = (variabelSatu, variabelDua) => {
    return variabelSatu / variabelDua;
};

// soal nomor 4 PERKALIAN
export const perkalian = (variabelSatu, variabelDua) => {
    return variabelSatu * variabelDua;
};

// soal nomor 5 RATARATA
export const ratarata = (variabelSatu, variabelDua, variabelTiga, variabelEmpat, variabelLima) => {
    return (variabelSatu + variabelDua + variabelTiga + variabelEmpat + variabelLima) / 5;
};

// soal nomor 6 SISA BAGI
export const sisabagi = (variabelSatu, variabelDua) => {
    return variabelSatu % variabelDua;
};

// soal nomor 7 LUAS PERSEGI
export const luaspersegi = (sisi) => {
    return sisi * sisi;
};

// soal nomor 8 LUAS LINGKARAN
export const luaslingkaran = (jarijari) => {
    return 3.14 * (jarijari * jarijari);
};

// soal nomor 9 LUAS KUBUS
export const luaskubus = (sisi) => {
    return 6 * (sisi * sisi);
};

// soal nomor 10 VOLUME KUBUS
export const volumekubus = (sisi) => {
    return sisi * sisi * sisi;
};